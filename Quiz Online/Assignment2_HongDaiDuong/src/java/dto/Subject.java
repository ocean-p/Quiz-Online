package dto;

import java.io.Serializable;

/**
 *
 * @author DELL
 */
public class Subject implements Serializable{
    private int id;
    private String code;
    private String des;

    public Subject() {
    }

    public Subject(int id, String code, String des) {
        this.id = id;
        this.code = code;
        this.des = des;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getDes() {
        return des;
    }

    public void setDes(String des) {
        this.des = des;
    }

    
}
